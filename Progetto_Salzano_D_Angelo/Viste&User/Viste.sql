--VISTA CHE VISUALIZZA ID FORNITURA, ID PRODOTTO, TAGLIA FORNITA E PREZZO DELLA FORNITURA
--PIU' COSTOSA

CREATE VIEW FORNITURA_MAX AS 
SELECT ID_FORNITURA, ID_PRODOTTO, TAGLIA_FORNITA , PREZZO_FORNITURA
FROM FORNITURA
WHERE PREZZO_FORNITURA = (SELECT MAX(PREZZO_FORNITURA) FROM FORNITURA)

-------------------------------------------------------------------------------------------------------------------------------------

--VISTA CHE VISUALIZZA ID FORNITURA, ID PRODOTTO, TAGLIA FORNITA E PREZZO DELLA FORNITURA
--MENO COSTOSA

CREATE VIEW FORNITURA_MIN AS 
SELECT ID_FORNITURA, ID_PRODOTTO, TAGLIA_FORNITA , PREZZO_FORNITURA
FROM FORNITURA
WHERE PREZZO_FORNITURA = (SELECT MIN(PREZZO_FORNITURA) FROM FORNITURA)

