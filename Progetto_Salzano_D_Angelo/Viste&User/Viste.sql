--VISTA CHE VISUALIZZA NOME,COGNOME, ID FORNITURA E DATA FORNITURA
--DELLE FORNITURE SPEDITE DURANTE GLI ULTIMI 3 MESI

CREATE VIEW ULTIME_FORN AS
SELECT FORNITORE.NOME_FORNITORE, FORNITORE.COGNOME_FORNITORE, 
FORNITURA.ID_FORNITURA, FORNITURA.DATA_FORNITURA 
FROM FORNITURA JOIN FORNITORE ON FORNITURA.AZIENDA_FORNITORE = FORNITORE.AZIENDA_FORNITORE
WHERE (SYSDATE - FORNITURA.DATA_FORNITURA) < 90

