--VISTA CHE VISUALIZZA CODICE PROMO, DATA INIZIO E DATA FINE DELLE PROMO
--CHE INIZIANO DAL 1° MAGGIO 2022 IN POI

CREATE VIEW PROSSIME_PROMO AS
