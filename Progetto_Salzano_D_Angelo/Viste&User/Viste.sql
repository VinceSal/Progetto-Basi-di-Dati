--VISTA CHE VISUALIZZA USERNAME, CODICE ACQUISTO E DATA ACQUISTO
--DI TUTTI GLI ACQUISTI EFFETTUATI NEL 2022

CREATE VIEW UTENTI_ACQUISTI AS
SELECT CARTA.CODICE_ACQUISTO, CARTA.USERNAME, ACQUISTO.DATA_ACQUISTO 
FROM CARTA JOIN ACQUISTO ON CARTA.CODICE_ACQUISTO = ACQUISTO.CODICE_ACQUISTO
WHERE ACQUISTO.DATA_ACQUISTO >= DATE'2022-01-01'

